export const PRINCIPLES = ["Perceivable", "Operable", "Understandable", "Robust"] as const;

export function getCriteriaByGuideline(principle: string) {
  const criteria = WCAG_CRITERIA.filter((c) => c.principle === principle);
  const guidelineMap = new Map<string, { guidelineTitle: string; criteria: WcagCriterion[] }>();
  for (const c of criteria) {
    if (!guidelineMap.has(c.guideline)) {
      guidelineMap.set(c.guideline, { guidelineTitle: c.guidelineTitle, criteria: [] });
    }
    guidelineMap.get(c.guideline)!.criteria.push(c);
  }
  return Array.from(guidelineMap.entries()).map(([guideline, val]) => ({ guideline, ...val }));
}
